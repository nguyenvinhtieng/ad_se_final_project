package com.lab.lab9.models;

public class Thu {
    private int idThu;
    private String tenThu;

    public Thu(int idThu, String tenThu) {
        this.idThu = idThu;
        this.tenThu = tenThu;
    }

    public int getIdThu() {
        return idThu;
    }

    public String getTenThu() {
        return tenThu;
    }
}
