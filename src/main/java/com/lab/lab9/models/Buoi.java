package com.lab.lab9.models;

public class Buoi {
    private int idBuoi;
    private String tenBuoi;

    public Buoi(int idBuoi, String tenBuoi) {
        this.idBuoi = idBuoi;
        this.tenBuoi = tenBuoi;
    }

    public int getIdBuoi() {
        return idBuoi;
    }

    public String getTenBuoi() {
        return tenBuoi;
    }
}
